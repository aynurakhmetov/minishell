#include "../minishell.h"

void	ft_redir(t_all *all)
{
	int k;
	int i;

	k = 0;
	i = -1;
	while (all->arg[++i] != 0)
	{
		if (ft_strncmp(all->arg[i], ">", 1) == 0)
			k++;
		if (ft_strncmp(all->arg[i], "<", 1) == 0)
			k++;
		if (ft_strncmp(all->arg[i], ">>", 1) == 0)
			k++;
	}
	k++;
	printf("%d\n", k);
}