#include "../minishell.h"

int	ft_env(t_all *all)
{
	int i;
	int j;

	i = -1;
	while (all->env[++i] != 0)
	{
		j = -1;
		while (all->env[i][++j] != 0)
		{
			if (all->env[i][j] == '=')
			{
				ft_putendl_fd(all->env[i], 1);
				continue ;			
			}
		}
	}

	// должен показывать b=""
	// Сравнить подробно с оригинальной выдачей b
	// Как работает с $?
	all->$_res = 0;
	return (0);
	
}